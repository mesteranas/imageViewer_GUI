    def closeEvent(self, event):
        if settings_handler.get("g","exitDialog")=="True":
            m=guiTools.ExitApp(self)
            m.exec()
            if m:
                event.ignore()
        else:
            self.close()
    def on_url(self):
        try:
            text,ok=qt.QInputDialog.getText(self,_("link"),_("enter link"))
            if ok:
                r=requests.get(text)
                pixmap=qt1.QPixmap()
                pixmap.loadFromData(r.content)
                self.image.setPixmap(pixmap)
        except:
            qt.QMessageBox.warning(self,_("error"),_("please try later"))
    def on_file(self):
        file=qt.QFileDialog(self)
        file.setAcceptMode(file.AcceptMode.AcceptOpen)
        if file.exec()==file.DialogCode.Accepted:
            pixmap=qt1.QPixmap(file.selectedFiles()[0])
            self.image.setPixmap(pixmap)
